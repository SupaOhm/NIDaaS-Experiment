import torch
import torch.nn as nn
from torch.utils.data import DataLoader, TensorDataset
import numpy as np
import os
try:
    from tqdm import tqdm
except ImportError:
    tqdm = lambda x, **kwargs: x

class _LSTMForecaster(nn.Module):
    def __init__(self, input_size, hidden_size):
        super().__init__()
        # Normalizes raw feature scale per-batch conceptually described in Section III-B-3
        self.bn = nn.BatchNorm1d(input_size)
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers=2, batch_first=True, dropout=0.2)
        # Final layer predicts the exact NEXT feature vector
        self.fc = nn.Linear(hidden_size, input_size)

    def forward(self, x):
        b, s, f = x.shape
        x_flat = x.reshape(b * s, f)
        x_norm = self.bn(x_flat)
        x = x_norm.reshape(b, s, f)
        out, _ = self.lstm(x)
        return self.fc(out[:, -1, :]) 

class LSTMEngine:
    def __init__(self, input_shape, hidden_size=64, lr=1e-3):
        
        # Safe binding prioritizing MPS -> GPU -> CPU
        if torch.backends.mps.is_available():
            # For massive datasets, MPS on Mac is highly efficient
            self.device = torch.device("mps")
        elif torch.cuda.is_available():
            self.device = torch.device("cuda")
        else:
            self.device = torch.device("cpu")
            
        print(f"[ids/lstm] Engine binding to compute device: {self.device}")
        
        self.net = _LSTMForecaster(input_shape[1], hidden_size).to(self.device)
        self.optimizer = torch.optim.Adam(self.net.parameters(), lr=lr)
        self.criterion = nn.MSELoss()
        self.tau = 0.5  # Default, overwritten during calibration

    def train(self, X_train, y_train, X_val, y_val, epochs=20, batch_size=4096, patience=3):
        print(f"[ids/lstm] Training exclusively on BENIGN traffic subsets...")
        X_t = torch.tensor(X_train, dtype=torch.float32).to(self.device)
        y_t = torch.tensor(y_train, dtype=torch.float32).to(self.device)
        X_v = torch.tensor(X_val, dtype=torch.float32).to(self.device)
        y_v = torch.tensor(y_val, dtype=torch.float32).to(self.device)

        loader = DataLoader(TensorDataset(X_t, y_t), batch_size=batch_size, shuffle=True)
        best_loss = float('inf')
        stamina = 0
        best_state = None

        for epoch in range(1, epochs + 1):
            self.net.train()
            train_loss = 0
            # Wrap in minimal loop (ignoring TQDM on older envs)
            for Xb, yb in tqdm(loader, desc=f"Epoch {epoch:02d}", leave=False):
                self.optimizer.zero_grad()
                loss = self.criterion(self.net(Xb), yb)
                loss.backward()
                self.optimizer.step()
                train_loss += loss.item() * len(Xb)
            
            self.net.eval()
            with torch.no_grad():
                val_loss = self.criterion(self.net(X_v), y_v).item()
                
            print(f"  Epoch {epoch:02d} | Train MSE: {train_loss/len(X_t):.4f} | Val MSE: {val_loss:.4f}")
            
            if val_loss < best_loss - 1e-4:
                best_loss = val_loss
                stamina = 0
                best_state = {k: v.cpu().clone() for k, v in self.net.state_dict().items()}
            else:
                stamina += 1
                if stamina >= patience:
                    print(f"  [Early Stop] Convergence reached at Epoch {epoch}.")
                    break
        
        if best_state is not None:
             self.net.load_state_dict(best_state)
             
        self.calibrate_threshold(X_v, y_v, batch_size)

    def calibrate_threshold(self, X_val_tensor, y_val_tensor, batch_size=4096):
        print("[ids/lstm] Calibrating dynamic anomaly threshold (mu + 3*sigma)...")
        d_val = self._score_batches(X_val_tensor, y_val_tensor, batch_size)
        self.tau = np.mean(d_val) + 3 * np.std(d_val)
        print(f"  --> Tau strictly locked to {self.tau:.4f}")

    def _score_batches(self, X_tensor, y_tensor, batch_size):
        self.net.eval()
        scores = []
        with torch.no_grad():
            for i in range(0, len(X_tensor), batch_size):
                preds = self.net(X_tensor[i:i+batch_size])
                # Euclidean distance reconstruction mapping
                dist = torch.norm(preds - y_tensor[i:i+batch_size], p=2, dim=1)
                scores.extend(dist.cpu().numpy())
        return np.array(scores)

    def predict(self, X_test, y_next, batch_size=4096):
        """Returns binary predictions based on locally calibrated threshold."""
        X_t = torch.tensor(X_test, dtype=torch.float32).to(self.device)
        y_t = torch.tensor(y_next, dtype=torch.float32).to(self.device)
        d_test = self._score_batches(X_t, y_t, batch_size)
        return (d_test > self.tau).astype(int)
